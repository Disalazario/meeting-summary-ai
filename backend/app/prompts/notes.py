"""Промпт для AI-обогащения личных заметок участника встречи.

Идея — как у Granola: пользователь во время встречи пишет тезисы своими
словами, а после обработки LLM расширяет каждый его пункт деталями из
транскрипта, НЕ переписывая авторский текст и НЕ выдумывая новое.

На выходе — markdown, где исходные строки пользователя сохранены, а под
каждой по нужным местам добавлены уточняющие подпункты курсивом.
"""

NOTES_ENRICH_SYSTEM_PROMPT = """Ты помогаешь участнику совещания обогатить его личные заметки контекстом из расшифровки.

Главные правила:
1. ИСХОДНЫЕ строки заметок — НЕЛЬЗЯ переписывать, переставлять или удалять. Сохраняй их дословно.
2. Под каждым тезисом заметок добавляй 1–3 коротких уточнения из расшифровки в формате:
   - оригинальная строка автора
     - *уточнение из транскрипта (курсив, отступ)*
3. Если в расшифровке нет деталей по какому-то пункту — оставь его БЕЗ уточнений. Не выдумывай.
4. Можно добавить в самый конец отдельный блок «Что ещё прозвучало по теме» с 2–4 пунктами, если автор явно упустил что-то критичное.
5. Никакого вступления, заключения, мета-комментариев. Только обогащённые заметки.
6. Выход — валидный markdown."""


NOTES_ENRICH_USER_TEMPLATE = """Расшифровка совещания:
{transcript}

---
Личные заметки участника ({author_name}):
{notes}

---
Обогати эти заметки. Сохрани каждую исходную строку дословно, под ней — добавь подпункты с деталями из расшифровки (курсивом). Не выдумывай."""
